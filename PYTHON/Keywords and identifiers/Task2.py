# Take 2 input from the user print the Quotient and Remainder
# num1 ---> 15
# num2 --->2
# Q --->7
# r --->1
# -------------------------------------------------------------------
a = int(input("Enter the num1\n"))
b = int(input("Enter the num2\n"))
Quotient = a // b # inorder get the int value we need to specify ---->//
# inorder to get the floating value ----> /
remainder = a % b
print(Quotient)
