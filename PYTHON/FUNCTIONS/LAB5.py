# THERE ARE TWO TYPES OF USER DEFINED- FUNCTIONS:
# FUNCTIONS WITHOUT ARGUMENTS
def say_hello():
    print("HEllo!")
say_hello()

# FUNCTIONS WITH ARGUMENTS AND PARAMETERS
def greeting_by_name(name):
    print("Hello",name)
    print(f"Hello,{name}")
greeting_by_name("sahana")
greeting_by_name(123)
greeting_by_name(3.14)

